package lesson3;

public class exampleSeven {
		
	public static void main(String[] args) {
		int numStudents = 15;


		// adds 3 students to existing variable
		numStudents = numStudents + 3;
				
		numStudents += 3;
		// does the same

		// *=, -=, +=, /=, %= are different forms of compound assignment

		numStudents++;

		numStudents--;

		
		numStudents -=10;
		numStudents *=2;
		numStudents /=3;
		numStudents++;	


	}
}
